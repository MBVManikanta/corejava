package datatypes;

public class characterliterals {

	public static void main(String[] args) {
		char ch1='A';//by using single codes
		char ch2=65;//ascii value of character 'A'
        char ch3='\u0041';//unicode format of 'A'
        char ch4='\n';
        System.out.println(ch1+"\n"+ch2+"\n"+ch3+"\n"+ch4);
	}

}
